package examen;
/*
Diseña dos funciones sobrecargadas de una función llamada 'resta'. Explica qué funcionalidad tendrán.
Dales una funcionalidad que esté relacionada con el nombre de la función.
 */
public class ejercicio_4 {
    public class Resta {
        public static int resta(int a, int b) {
            return a - b;
        }

        public static double resta(double a, double b) {
            return a - b;
        }
    }
}
